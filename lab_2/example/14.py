#Comparison Operators
#Comparison operators are used to compare two values

#Equal:
x = 5
y = 3
print(x == y) #False

#Not equal:
x = 5
y = 3
print(x != y) #True

#Greater than:
x = 5
y = 3
print(x > y) #True

#Less than:
x = 5
y = 3
print(x < y) #False

#Greater than or equal to:
x = 5
y = 3
print(x >= y) #True

#Less than or equal to:
x = 5
y = 3
print(x <= y) #False